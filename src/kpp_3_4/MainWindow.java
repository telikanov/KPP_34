    private void btnLoadTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadTxtActionPerformed
        FileDialog fd = new FileDialog(this, "Choose a file", FileDialog.LOAD);
        fd.setVisible(true);
        String filename = fd.getDirectory() + fd.getFile();

        AthletesList.clear();
        Athlete tempAthlete;
        Path file = Paths.get(filename);
        java.util.List<String> lines = null;
        try {
            Charset charset = Charset.forName("UTF-8");
            lines = Files.readAllLines(file, charset);
        } catch (IOException e) {
        }

        String[] tempStrList;
        for (String line : lines) {
            if (line.length() < 5) {
                break;
            }
            tempStrList = line.split("\\|");

            tempAthlete = new Athlete();

            tempAthlete.FIO = tempStrList[0];
            tempAthlete.Rank = tempStrList[1];
            System.out.println(tempAthlete.Rank);
            tempAthlete.Heft = tempStrList[2];
            tempAthlete.CountCompetition = tempStrList[3];
            tempAthlete.NumberPoint = tempStrList[4];
            tempAthlete.Day = Integer.parseInt(tempStrList[5]);
            tempAthlete.Month = Integer.parseInt(tempStrList[6]);
            tempAthlete.Year = Integer.parseInt(tempStrList[7]);

            AthletesList.add(tempAthlete);
        }

        ShowAllAthletes();
    }//GEN-LAST:event_btnLoadTxtActionPerformed

    private void btnLoadXmlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadXmlActionPerformed
        FileDialog fd = new FileDialog(this, "Choose a file", FileDialog.LOAD);
        fd.setVisible(true);
        String filename = fd.getDirectory() + fd.getFile();

        AthletesList.clear();

        try {
            FileInputStream inputStream = new FileInputStream(filename);
            XMLDecoder decoder = new XMLDecoder(inputStream);
            AthletesList = (ArrayList<Athlete>) decoder.readObject();
            decoder.close();
        } catch (Exception ex) {
        }

        ShowAllAthletes();
    }//GEN-LAST:event_btnLoadXmlActionPerformed

    private void btnSaveXmlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveXmlActionPerformed
        FileDialog fd = new FileDialog(this, "Choose a file", FileDialog.SAVE);
        fd.setVisible(true);
        String filename = fd.getDirectory() + fd.getFile();

        try {
            FileOutputStream outputStream = new FileOutputStream(filename);
            XMLEncoder encoder = new XMLEncoder(outputStream);
            encoder.writeObject(AthletesList);
            encoder.close();
            Thread.sleep(10000);
        } catch (Exception ex) {
        }

    }//GEN-LAST:event_btnSaveXmlActionPerformed

    public void ShowAllAthletes() {
        DefaultTableModel model = (DefaultTableModel) tableAthletes.getModel();
        model.setRowCount(0);
        AthletesList.forEach((newAthlete) -> {
            model.addRow(new Object[]{newAthlete.FIO, newAthlete.Rank, newAthlete.Heft, newAthlete.CountCompetition, newAthlete.NumberPoint, newAthlete.Day, newAthlete.Month, newAthlete.Year});
        });

        Runnable task = () -> {
            double Count = Double.valueOf(AthletesList.size());
            
            double gem = 1;
            for (Athlete a : AthletesList) {
                gem *= Double.valueOf(a.NumberPoint);   
            }
            if (Count != 0) {
                lblAgeAveValue.setText(String.valueOf(Math.pow(gem,1/Count)));
                System.out.println(gem);
            } else {
                lblAgeAveValue.setText(String.valueOf(0));
            }
        };

        Thread thread = new Thread(task);
        thread.start();

    }

