    private void btnSaveTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveTxtActionPerformed
        FileDialog fd = new FileDialog(this, "Choose a file", FileDialog.SAVE);
        fd.setVisible(true);
        String filename = fd.getDirectory() + fd.getFile();

        ArrayList<String> lines;
        lines = new ArrayList();

        String tmp;
        for (Athlete a : AthletesList) {
            tmp = a.FIO + '|' + a.Rank + '|'
                    + a.Heft + '|' + a.CountCompetition + '|'
                    + a.NumberPoint + '|' + String.valueOf(a.Day) + '|'
                    + String.valueOf(a.Month) + '|' + String.valueOf(a.Year);

            lines.add(tmp);
        }

        Path file = Paths.get(filename);
        try {
            Files.write(file, lines, Charset.forName("UTF-8"));
        } catch (IOException e) {
        }

    }//GEN-LAST:event_btnSaveTxtActionPerformed

