    private void btnAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewActionPerformed
        Athlete newAthlete = new Athlete();
        newAthlete.FIO = newFIO.getText();
        newAthlete.Rank = newRank.getText();
        newAthlete.Heft = newHeft.getText();
        newAthlete.CountCompetition = newCountCompetition.getText();
        newAthlete.NumberPoint = newNumberPoint.getText();
        newAthlete.Day = (int) newDay.getValue();
        newAthlete.Month = (int) newMonth.getValue();
        newAthlete.Year = (int) newYear.getValue();

        AthletesList.add(newAthlete);
        ShowAllAthletes();

        newFIO.setText("");
        newRank.setText("");
        newHeft.setText("");
        newCountCompetition.setText("");
        newNumberPoint.setText("");
    }//GEN-LAST:event_btnAddNewActionPerformed

