    private void btnFIOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFIOActionPerformed
        searchAthletes.clear();
        for (Athlete a : AthletesList) {
            if (a.FIO.contains(editSearch.getText())) {
                searchAthletes.add(a);
            }
        }
        //Show search
        DefaultTableModel model = (DefaultTableModel) tableAthletes.getModel();
        model.setRowCount(0);
        searchAthletes.forEach((newAthlete) -> {
            model.addRow(new Object[]{newAthlete.FIO, newAthlete.Rank, newAthlete.Heft, newAthlete.CountCompetition, newAthlete.NumberPoint, newAthlete.Day, newAthlete.Month, newAthlete.Year});
        });
    }//GEN-LAST:event_btnFIOActionPerformed

